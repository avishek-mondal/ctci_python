def queens(grid_size):
    result = []
    board = [[EMPTY_CHAR for _ in range(grid_size)] for _ in range(grid_size)]
    queen_helper(
        row_num=0, board=board, grid_size=grid_size, result=result
    )
    return result


def queen_helper(
    row_num,
    board,
    grid_size,
    result,
    queen_char=QUEEN_CHAR,
    empty_char=EMPTY_CHAR,
):
    if row_num == grid_size:
        result.append(deepcopy(board))
        return
    for col_num in range(grid_size):
        if position_is_valid(board, row_num, col_num, queen_char):
            board[row_num][col_num] = queen_char
            # pprint(board, row_num, col_num)
            queen_helper(
                row_num=row_num + 1,
                board=board,
                grid_size=grid_size,
                result=result,
                queen_char=queen_char,
            )
            board[row_num][col_num] = empty_char


def position_is_valid(board, row_num, col_num, queen_char):
    return (
        row_is_valid(board, row_num, col_num, queen_char)
        and col_is_valid(board, row_num, col_num, queen_char)
        and diagonals_are_valid(board, row_num, col_num, queen_char)
    )


def row_is_valid(board, row_num, col_num, queen_char):
    for col in range(col_num):
        if board[row_num][col] == queen_char:
            return False
    return True


def col_is_valid(board, row_num, col_num, queen_char):
    for row in range(row_num):
        if board[row][col_num] == queen_char:
            return False
    return True


def diagonals_are_valid(board, row_num, col_num, queen_char):
    return upper_left_valid(
        board, row_num, col_num, queen_char
    ) and upper_right_valid(board, row_num, col_num, queen_char)


def upper_left_valid(board, row_num, col_num, queen_char):
    r, c = row_num, col_num
    while r >= 0 and c >= 0:
        if board[r][c] == queen_char:
            return False
        r -= 1
        c -= 1
    return True


def upper_right_valid(board, row_num, col_num, queen_char):
    r, c = row_num, col_num
    while r >= 0 and c < len(board[row_num]):
        if board[r][c] == queen_char:
            return False
        r -= 1
        c += 1
    return True


